SUBROUTINE tide_perm (dC20_perm)


! ----------------------------------------------------------------------
! SUBROUTINE: tide_perm.f90
! ----------------------------------------------------------------------
! Purpose:
!  Permanent Tide term
! ----------------------------------------------------------------------
! Input arguments
!
! Output arguments:
! - dC20_perm :		Permanent tide (time-independent) as C20 coefficient correction
! ----------------------------------------------------------------------
! Dr. Thomas Papanikolaou, Geoscience Australia            November 2015
! ----------------------------------------------------------------------


      USE mdl_precision
      IMPLICIT NONE

! ----------------------------------------------------------------------
! Dummy arguments declaration
! ----------------------------------------------------------------------
! IN

! OUT
      REAL (KIND = prec_q), INTENT(OUT) :: dC20_perm
! ----------------------------------------------------------------------

! ----------------------------------------------------------------------
! Local variables declaration
! ----------------------------------------------------------------------  
      REAL (KIND = prec_q) :: k20
! ----------------------------------------------------------------------


! ----------------------------------------------------------------------
! Love numbers (Knm) : Table 6.3, IERS Conventions (2010)
! ----------------------------------------------------------------------
! Elastic Earth
      k20 = 0.29525D0
! Anelastic Earth
      k20 = 0.30190D0
! ----------------------------------------------------------------------

! ----------------------------------------------------------------------
! Permanent tide (time-independent): C20 coefficient correction
      dC20_perm = 4.4228D-8 * (-0.31460D0) * k20
! ----------------------------------------------------------------------


END
